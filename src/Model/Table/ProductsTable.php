//            ->scalar('productimage')
//            ->maxLength('productimage', 200)
//            ->allowEmpty('productimage');
//
//        $validator
//            ->scalar('status')
//            ->maxLength('status', 60)
//            ->requirePresence('status', 'create')
//            ->notEmpty('status');

        return $validator;
    }
}
